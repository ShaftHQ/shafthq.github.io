import {mkdir, readFile, writeFile} from 'node:fs/promises';
import path from 'node:path';
import {fileURLToPath} from 'node:url';
import {buildDocumentationIndex} from '../netlify/functions/docs-loader.mjs';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const docsRoot = path.join(root, 'docs');
const configPath = path.join(root, 'docusaurus.config.js');
const llmsTxtPath = path.join(root, 'static', 'llms.txt');
const llmsFullTxtPath = path.join(root, 'static', 'llms-full.txt');

// docusaurus.config.js mixes `require()` with a top-level `export default`,
// which only runs through Docusaurus's own config loader (esbuild-backed
// bundle-require), not plain Node ESM `import()`. Rather than duplicate that
// loader, read the handful of literals we need out of the source text so the
// site name/URL stay driven by the real config instead of a second hardcoded
// copy of them.
async function readSiteConfig() {
  const source = await readFile(configPath, 'utf8');

  const siteUrlMatch = source.match(/const siteUrl = ['"]([^'"]+)['"]/);
  const baseUrlMatch = source.match(/\bbaseUrl:\s*['"]([^'"]+)['"]/);
  const titleMatch = source.match(/\n\s*title:\s*['"]([^'"]+)['"]/);
  const descriptionMatch = source.match(/name:\s*['"]description['"],\s*\n\s*content:\s*['"]([^'"]+)['"]/);

  if (!siteUrlMatch) throw new Error(`Could not find "const siteUrl = ..." in ${configPath}`);

  return {
    siteUrl: siteUrlMatch[1],
    baseUrl: baseUrlMatch?.[1] ?? '/',
    siteName: titleMatch?.[1] ?? 'SHAFT User Guide',
    siteDescription: descriptionMatch?.[1]
      ?? 'SHAFT is a unified test automation engine for Web, Mobile, API, CLI, and Database testing.',
  };
}

// Sidebar top-level categories (sidebars.js), in display order. Reference and
// Maintainers are `{type: 'autogenerated', dirName: ...}` sections, so their
// membership is just "everything under that docs/ subdirectory".
const CATEGORY_ORDER = ['start', 'testing', 'agentic', 'features', 'integrations', 'reference', 'maintainers'];
const CATEGORY_LABELS = {
  start: 'Start',
  testing: 'Testing',
  agentic: 'Agentic',
  features: 'Features',
  integrations: 'Integrations',
  reference: 'Reference',
  maintainers: 'Maintainers',
};

function unquote(value) {
  const trimmed = value.trim();
  if ((trimmed.startsWith('"') && trimmed.endsWith('"')) || (trimmed.startsWith("'") && trimmed.endsWith("'"))) {
    return trimmed.slice(1, -1);
  }
  return trimmed;
}

function parseFrontmatter(content) {
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  const block = match?.[1] ?? '';
  const field = (name) => {
    const fieldMatch = block.match(new RegExp(`^${name}:\\s*(.+)$`, 'm'));
    return fieldMatch ? unquote(fieldMatch[1]) : undefined;
  };
  const sidebarPosition = field('sidebar_position');
  return {
    title: field('title'),
    description: field('description'),
    slug: field('slug'),
    sidebarPosition: sidebarPosition === undefined ? Number.POSITIVE_INFINITY : Number(sidebarPosition),
  };
}

function docRoute(relativePath, frontmatterSlug) {
  if (frontmatterSlug) {
    const normalized = frontmatterSlug.startsWith('/') ? frontmatterSlug : `/${frontmatterSlug}`;
    return `/docs${normalized}`;
  }
  const withoutExtension = relativePath.replace(/\.(?:md|mdx)$/u, '');
  return `/docs/${withoutExtension}`;
}

async function loadPages(siteUrl, baseUrl) {
  // Reuse the same corpus the AutoBot chatbot is built from: buildDocumentationIndex()
  // already walks docs/, excludes docs/archive/** and unlisted pages, and strips
  // frontmatter/JSX/imports into clean markdown chunks per heading.
  const chunks = buildDocumentationIndex();

  const chunksByPath = new Map();
  for (const chunk of chunks) {
    if (!chunksByPath.has(chunk.path)) chunksByPath.set(chunk.path, []);
    chunksByPath.get(chunk.path).push(chunk);
  }

  const absoluteBase = new URL(baseUrl, `${siteUrl.replace(/\/$/u, '')}/`);
  const pages = [];

  for (const [relativePath, pathChunks] of chunksByPath) {
    const category = relativePath.split('/')[0];
    if (!CATEGORY_LABELS[category]) {
      console.warn(`[build-llms-txt] Skipping "${relativePath}": unmapped top-level category "${category}".`);
      continue;
    }

    const rawContent = await readFile(path.join(docsRoot, ...relativePath.split('/')), 'utf8');
    const frontmatter = parseFrontmatter(rawContent);
    if (!frontmatter.title) {
      console.warn(`[build-llms-txt] Skipping "${relativePath}": no frontmatter title.`);
      continue;
    }

    const route = docRoute(relativePath, frontmatter.slug);
    const url = new URL(route.replace(/^\//u, ''), absoluteBase).toString();

    pages.push({
      path: relativePath,
      category,
      title: frontmatter.title,
      description: frontmatter.description ?? '',
      sidebarPosition: frontmatter.sidebarPosition,
      url,
      content: pathChunks.map((chunk) => chunk.content).join('\n\n'),
    });
  }

  pages.sort((a, b) => {
    const categoryDiff = CATEGORY_ORDER.indexOf(a.category) - CATEGORY_ORDER.indexOf(b.category);
    if (categoryDiff !== 0) return categoryDiff;
    if (a.sidebarPosition !== b.sidebarPosition) return a.sidebarPosition - b.sidebarPosition;
    return a.title.localeCompare(b.title);
  });

  return pages;
}

function buildLlmsTxt(pages, siteName, siteDescription) {
  const lines = [`# ${siteName}`, '', `> ${siteDescription}`, ''];

  for (const category of CATEGORY_ORDER) {
    const categoryPages = pages.filter((page) => page.category === category);
    if (categoryPages.length === 0) continue;

    lines.push(`## ${CATEGORY_LABELS[category]}`);
    for (const page of categoryPages) {
      const description = page.description ? `: ${page.description}` : '';
      lines.push(`- [${page.title}](${page.url})${description}`);
    }
    lines.push('');
  }

  return `${lines.join('\n').trim()}\n`;
}

function buildLlmsFullTxt(pages, siteName, siteDescription) {
  const sections = [`# ${siteName}`, '', `> ${siteDescription}`, ''];

  for (const page of pages) {
    sections.push(
      '---',
      '',
      `# ${page.title}`,
      '',
      `Canonical URL: ${page.url}`,
      '',
      page.content,
      '',
    );
  }

  return `${sections.join('\n').trim()}\n`;
}

const {siteUrl, baseUrl, siteName, siteDescription} = await readSiteConfig();
const pages = await loadPages(siteUrl, baseUrl);

if (pages.length === 0) throw new Error('No pages were collected for llms.txt; refusing to write empty output.');

const llmsTxt = buildLlmsTxt(pages, siteName, siteDescription);
const llmsFullTxt = buildLlmsFullTxt(pages, siteName, siteDescription);

await mkdir(path.dirname(llmsTxtPath), {recursive: true});
await writeFile(llmsTxtPath, llmsTxt, 'utf8');
await writeFile(llmsFullTxtPath, llmsFullTxt, 'utf8');

console.log(`Wrote ${pages.length} pages to static/llms.txt (${Buffer.byteLength(llmsTxt)} bytes).`);
console.log(`Wrote ${pages.length} pages to static/llms-full.txt (${Buffer.byteLength(llmsFullTxt)} bytes).`);
